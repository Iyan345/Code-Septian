package UTS;

public class substring {
    public static void main(String[] args){
        String alfabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZ#-";

        System.out.println(alfabet);
        System.out.println("" + alfabet.substring(18,19));
        System.out.println("" + alfabet.substring(4,5));
        System.out.println("" + alfabet.substring(15,16));
        System.out.println("" + alfabet.substring(19,20));
        System.out.println("" + alfabet.substring(8,9));
        System.out.println("" + alfabet.substring(0,1));
        System.out.println("" + alfabet.substring(13,14));
    }
}
